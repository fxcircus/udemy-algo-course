/* Tree Traversal:
==================
Traversing a tree = visiting every node ONLY 1 time.
There are many ways to do this, we will focus on 4:
-   Breadth First Search (BFS)
-   Depth First PreOrder
-   Depth First PostOrder
-   Depth First InOrder

BFS is "horizontal" \ on the same level ----->
SDF is "vertical" \ different level each time... 

Example:
========
In order to find if 99 is in the tree below,
We will need to travese the tree.

    ________10_______
    |               |
____19____          6____
|       |               |
99      8               20

*/